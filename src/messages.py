class HaveMessage:
	def __init__(self, piece_index):
		self.len = "\x00\x00\x00\x05"
		self.id = "\x04"
		self.payload = piece_index
		if len(self.payload) > int(self.len.encode("hex", 16)) - 1:
			raise Exception("Payload does not match declared message length")

